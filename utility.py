    '''
    :param value: string or list of length = 81
            (strings are converted to list)
            members of value in g.VALID_GRID_ENTRIES
    :return:
    '''
    assert len(value) == g.MAX_CELLS
    # todo how to assert following
    # assert items in value in g.VALID_GRID_ENTRIES
    try:
        if type(value) == type(STRING_TYPE):
            value = list(value)
        pstr = title + g.NL
        title_length = len(title)
        i = 0
        length = 0
        is_once = True
        for m in range(3):
            for j in range(3):
                for k in range(9):
                    if k == 3 or k == 6:
                        pstr += '| '
                    pstr += (value[i] + ' ')
                    i += 1
                pstr = pstr[:-1] + g.NL
                if is_once:
                    length = len(pstr) - title_length - 2
                    is_once = False
                if m == 0 and j == 2 or m == 1 and j == 2:
                    pstr += '-' * length + g.NL
        print()
        print(pstr)
        #
    except Exception as e:
        print(e)
        print('in codec.print_grid()')
        sys.exit()
